//display data write path define function
void TFT_DP_DATA(uint16_t Data)
{
	PORTC = (uint8_t) (Data & 0x00FF);
	PORTA = (uint8_t) ((Data & 0xFE00)>>8);
	if (Data & (1<<8)) PORTB |=(1<<0);
	else PORTB &=~(1<<0);
}

